import Foundation
import UIKit

extension UIColor {
    
    open static var darkBlueNavColor: UIColor = {
        return UIColor(red: 63/255.0, green: 93/255.0, blue: 161/255.0, alpha: 1.0)
    }()
    
    open static var lightBlueColor: UIColor = {
        return UIColor(red: 0/255.0, green: 122/255.0, blue: 255/255.0, alpha: 1.0)
    }()
    
    open static var lightGrayColor: UIColor = {
        return UIColor(red:0.97, green:0.97, blue:0.97, alpha:1.0)
    }()
    
}
